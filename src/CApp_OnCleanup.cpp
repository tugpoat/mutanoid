#include "CApp.h"

//==============================================================================
void CApp::OnCleanup() {

    //--------------------------------------------------------------------------
    // Entities
    //--------------------------------------------------------------------------
    for(unsigned int i = 0;i < CEntity::EntityList.size();i++) {
        if(!CEntity::EntityList[i]) continue;

        CEntity::EntityList[i]->OnCleanup();
    }

    SDL_FreeSurface(Surf_Display);
    SDL_DestroyWindow(mWindow);
    IMG_Quit();
    SDL_Quit();
}

